const translations: Record<Language, Dictionary> = {
  en: englishTranslations,
  mm: { ...englishTranslations, ...mmOverrides },
  zh: { ...englishTranslations, ...zhOverrides },
  th: { ...englishTranslations, ...thOverrides },
};

function interpolate(template: string, params?: TranslationParams) {
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (_, token: string) => {
    const value = params[token];
    return value == null ? `{${token}}` : String(value);
  });
}

export function translate(language: Language, key: string, params?: TranslationParams) {
  const dictionary = translations[language] ?? translations.en;
  const template = dictionary[key] ?? translations.en[key] ?? key;
  return interpolate(template, params);
}

export function hasTranslation(language: Language, key: string) {
  return Boolean(translations[language]?.[key] ?? translations.en[key]);
}

export function useI18n() {
  const { language } = useLanguage();

  const api = useMemo(
    () => ({
      language,
      t: (key: string, params?: TranslationParams) => translate(language, key, params),
      has: (key: string) => hasTranslation(language, key),
    }),
    [language]
  );

  return api;
}

export type { TranslationParams };
